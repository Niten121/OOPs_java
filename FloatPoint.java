public class FloatPoint {
    public static void main(String[] args) {
        float myNum1 = 5.75f, myNum2 = 3.20f;
        float addition = myNum1 + myNum2;
        float substraction = myNum1 - myNum2;
        float multiplication = myNum1 * myNum2;
        float division = myNum1 / myNum2;
        float modulo_division = myNum1 % myNum2;
        System.out.println("1 number:-> " + myNum1);
        System.out.println("2 number:-> " + myNum2);
        System.out.println("\nAddition:-> " + addition);
        System.out.println("Substraction:-> " + substraction);
        System.out.println("Multiplication:-> " + multiplication);
        System.out.println("Division:-> " + division);
        System.out.println("Modulo_division:-> " + modulo_division);
    }
}
